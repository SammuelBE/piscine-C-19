int			ft_sqrt(int nb)
{
	int x;
	int y;

	x = nb;
	y = 1;
	if (nb < 0)
		return (0);
	while (x - y > 1)
	{
		x = (x + y) / 2;
		y = nb / x;
	}
	if (nb == x * x)
		return (x);
	else
		return (0);
}
